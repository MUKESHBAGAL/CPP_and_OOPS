#include<iostream>
using namespace std;
int main(){
	int x,y;
	x=10;
	y=12;
	x++;
	y--;
	cout<<"After Increment the number(Used Increment Operator): "<<x<<endl;
	cout<<"After Decrement the number(Used Decrment Operator): "<<y<<endl;
	
	return 0;
}

/*
OUTPUT::

	After Increment the number(Used Increment Operator): 11
	After Decrement the number(Used Decrment Operator): 11

*/
